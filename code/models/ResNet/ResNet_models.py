class BasicConv2d(nn.Module):
    def __init__(self, in_planes, out_planes, kernel_size, stride=1, padding=0, dilation=1):
        super(BasicConv2d, self).__init__()
        self.conv = nn.Conv2d(in_planes, out_planes,
                              kernel_size=kernel_size, stride=stride,
                              padding=padding, dilation=dilation, bias=False)
        self.bn = nn.BatchNorm2d(out_planes)
        self.relu = nn.ReLU(inplace=True)

    def forward(self, x):
        x = self.conv(x)
        x = self.bn(x)
        return x

class Classifier_Module(nn.Module):
    def __init__(self,dilation_series,padding_series,NoLabels, input_channel):
        super(Classifier_Module, self).__init__()
        self.conv2d_list = nn.ModuleList()
        for dilation,padding in zip(dilation_series,padding_series):
            self.conv2d_list.append(nn.Conv2d(input_channel,NoLabels,kernel_size=3,stride=1, padding =padding, dilation = dilation,bias = True))
        for m in self.conv2d_list:
            m.weight.data.normal_(0, 0.01)

    def forward(self, x):
        out = self.conv2d_list[0](x)
        for i in range(len(self.conv2d_list)-1):
            out += self.conv2d_list[i+1](x)
        return out

class Descriptor(nn.Module):
    def __init__(self, channel):
        super(Descriptor, self).__init__()
        self.channel_axis = 1
        self.num_fc = 100
        self.name = 'descriptor'

        self.conv1 = nn.Conv2d(4, channel, 3, 1, 1, bias=False)
        self.conv2 = nn.Conv2d(channel, channel * 2, 4, 2, 1, bias=False)
        self.conv3 = nn.Conv2d(channel * 2, channel * 4, 4, 2, 1, bias=False)
        self.conv4 = nn.Conv2d(channel * 4, channel * 8, 4, 2, 1, bias=False)
        self.conv5 = nn.Conv2d(channel * 8, 1, 4, 1, 0)

        self.bn1 = nn.BatchNorm2d(channel)
        self.bn2 = nn.BatchNorm2d(channel*2)
        self.bn3 = nn.BatchNorm2d(channel*4)
        self.bn4 = nn.BatchNorm2d(channel*8)
        #self.bn5 = nn.BatchNorm2d(1)

        self.leaky_relu = nn.LeakyReLU(negative_slope=0.2, inplace=True)

        self.fc1 = nn.Linear(1*1*29*29, self.num_fc)
        self.fc2 = nn.Linear(1 * 1 * 41 * 41, self.num_fc)
        self.fc3 = nn.Linear(1 * 1 * 53 * 53, self.num_fc)

    def forward(self, input, seg):
        feature_map = torch.cat((input, seg), 1)
        x = self.conv1(feature_map)
        x = self.bn1(x)
        x = self.leaky_relu(x)
        x = self.conv2(x)
        x = self.bn2(x)
        x = self.leaky_relu(x)
        x = self.conv3(x)
        x = self.bn3(x)
        x = self.leaky_relu(x)
        x = self.conv4(x)
        x = self.bn4(x)
        x = self.leaky_relu(x)
        x = self.conv5(x)
        if x.shape[2] == 29:
            x = x.view(-1, 1 * 1 * 29 * 29)
            feat = self.fc1(x)
        elif x.shape[2] == 41:
            x = x.view(-1, 1 * 1 * 41 * 41)
            feat = self.fc2(x)
        else:
            x = x.view(-1, 1 * 1 * 53 * 53)
            feat = self.fc3(x)
        return feat

